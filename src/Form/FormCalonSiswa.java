public void TampilData() {
        DftTblModel_barang = new DefaultTableModel();
        DftTblModel_barang.addColumn("NISN");
        DftTblModel_barang.addColumn("NAMA");
        DftTblModel_barang.addColumn("TEMPAT LAHIR");
        DftTblModel_barang.addColumn("JENIS KELAMIN");
        DftTblModel_barang.addColumn("AGAMA");
        DftTblModel_barang.addColumn("ALAMAT");
        DftTblModel_barang.addColumn("NAMA ORANG TUA");
        DftTblModel_barang.addColumn("TELEPON");
        jTable_barang.setModel(DftTblModel_barang);
        Connection conn = Koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from calon_siswa";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_barang.addRow(new Object[]{
                    res.getString("NISN"),
                    res.getString("nama_lengkap"),
                    res.getString("tempat_lahir"),
                    res.getString("jenis_kelamin"),
                    res.getString("agama"),
                    res.getString("alamat"),
                    res.getString("nama_ortu"),
                    res.getString("telepon"),
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
